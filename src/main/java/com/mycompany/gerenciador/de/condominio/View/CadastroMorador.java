    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = nomePessoa.getText();
        int idade = Integer.parseInt(idadePessoa.getText());
        String cpf = cpfPessoa.getText();
        String rg = rgPessoa.getText();
        CPFAdapter validator = new CPFAdapter(cpf);
        try {
            if(!validator.validateCpf()){
             JOptionPane.showMessageDialog(rootPane, "Este CPF nÃ£o Ã© vÃ¡lido.");
            return;
            }    
            if(nome.isEmpty() || idadePessoa.getText().isEmpty() || cpf.isEmpty() || rg.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Nao Ã© possivel cadastrar com informaÃ§Ãµes nulas");
                return;
            }
            if(pc.create(nome, idade, cpf, rg)){
                JOptionPane.showMessageDialog(rootPane, "Morador cadastrado com sucesso");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro Ao Cadastrar");
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

